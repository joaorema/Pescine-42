#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	l;
	int	a;

	a = argc - 1;
	while (a > 0)
	{
		l = 0;
		while (argv[a][l] != '\0')
		{
			write(1, &argv[a][l], 1);
			l++;
		}
		write(1, "\n", 1);
		--a;
	}
	return (0);
}
